ORG C0
DB "AEIOU"
DB 00

ORG 00
MOV AL,FF
PUSH AL
MOV AL,C0
MOV CL,20
MOV DL,C0

Scanning:
	MOV BL,[AL]
	CMP BL,41
	JZ VOWEL
	CMP BL,45
	JZ VOWEL
	CMP BL,49
	JZ VOWEL
	CMP BL,4F
	JZ VOWEL
	CMP BL,55
	JZ VOWEL
	INC AL
	CMP BL,00
	JNZ Scanning
	JMP CLEAR

VOWEL:
	PUSH BL
	INC AL
	JMP Scanning

CLEAR:
	MOV AL,[DL]
	MOV [DL],CL
	CMP AL,00
	JZ RESTORE
	INC DL
	JMP CLEAR

RESTORE:
	POP CL
	CMP CL,FF
	JZ END
	MOV [AL],CL
	INC AL
	JMP RESTORE
	
END:
	END 