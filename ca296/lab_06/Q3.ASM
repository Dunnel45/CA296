;------------------ PROGRAM WITH PROCEDURE -------------------
CALL 50 ;EXECUTE PROCEDURE AT 50
HALT
;------------------ PLACE ASTERIX ON VDU ---------------------
ORG 50 ;PLACE PROCEDURE
PUSH AL
PUSH BL
PUSH CL
PUSH DL
PUSHF
PLACE:
	MOV AL,D5 ;PUT LOCATION OF CODE INTO AL
	MOV BL,2B ;PUT CHAR YOU WANT TO PUT INTO THAT POSITION INTO BL
	MOV [AL],BL ;MOVE CHAR IN BL INTO LOCATION IN AL
	POPF	;RESTORE SP AND REGISTERS
	POP DL
	POP CL
	POP BL
	POP AL
	RET	;RETURN TO PROGRAM

END