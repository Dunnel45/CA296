;------------------ PROGRAM WITH PROCEDURE -------------------
CALL 50 ;EXECUTE PROCEDURE AT 50
HALT
;------------------ PLACE ASTERIX ON VDU ---------------------
ORG 50 ;PLACE PROCEDURE
PUSH AL
PUSH BL
PUSH CL
PUSHF
PLACE:
	MOV AL,C5
	MOV BL,2B
	PUSH AL
	PUSH BL
	POP BL
	POP AL
	MOV [AL],BL
	POPF	;RESTORE SP AND REGISTERS
	POP DL
	POP CL
	POP BL
	POP AL
	RET	;RETURN TO PROGRAM

END