JMP MAINPROG	;JUMP OVER VECTOR TABLE
DB 00
DB 30

MAINPROG:
	MOV BL,60 ;MOVE STARTING LOCATION OF TABLE INTO BL 
	CMP CL,02
	JZ LOOP
	OUT 07
	IN 07
	PUSH AL
	INC CL
	JMP MAINPROG

LOOP:	;INFINITE LOOP WHICH KEEPS INCREMENTING CL UNTIL TIMER GOES OFF
	POP AL
	POP CL
	ADD AL,30
	ADD CL,30


ORG 30  ;PLACE INTERRUPT CODE AT LOCATION 20
CLI  ;TURN OFF INTERRUPTS ONCE IN ISR
	
DISPLAY:
	MOV AL,[AL] ;MOVE NUMBERS IN TABLE INTO AL
	OUT 02	;PLACE HEX NUMBER IN AL ONTO 7 SEGMENT DISPLAY
	PUSH CL
	POP AL
	MOV AL,[AL]
	DEC AL
	OUT 02
	JMP END
	

END:
	MOV AL,00 ;CLEAR 7 SEGMENT DISPLAY
	OUT 02
	MOV AL,01
	OUT 02
	HALT


ORG 60	;PLACE TABLE AT 10
DB FB	;HEX FOR NUMBER 9 ON 7 SEGMENT DISPLAY
DB 61
DB B7
DB 9F
DB 4F
DB DD
DB FD
DB 8B
DB FF
DB DF
END